namespace PathsFinder;

/// <summary>
/// Represents the current geographic location with latitude and longitude coordinates.
/// </summary>
public class CurrentLocation
{
    /// <summary>
    /// Gets or sets the latitude of the current location.
    /// </summary>
    public double Latitude { get; set; }

    /// <summary>
    /// Gets or sets the longitude of the current location.
    /// </summary>
    public double Longitude { get; set; }
}